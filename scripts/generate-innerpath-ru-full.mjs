for (const w of raw.innerWays) {
  const t = TEXTS[w.id];
  if (!t) {
    console.warn('нет перевода:', w.id);
    continue;
  }
  out[w.id] = {
    title: TITLES[w.id] || w.nameEn,
    pathCategory: PATH[w.pathEn] || w.pathEn,
    effect: t.effect,
    howToGet: t.howToGet,
  };
}

const dest = path.join(__dirname, 'data', 'innerPathRu.json');
fs.mkdirSync(path.dirname(dest), { recursive: true });
fs.writeFileSync(dest, JSON.stringify(out, null, 2), 'utf8');
